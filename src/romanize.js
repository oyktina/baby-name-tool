export function romanizeGiven(given) {
  if (!given) return "";
  const chars = [...given];
  if (chars.length === 1) return ROMA[chars[0]] || chars[0];
  if (chars.length === 2) {
    const a = ROMA[chars[0]] || chars[0];
    const b = ROMA[chars[1]] || chars[1];
    return `${a}-${b}`;
  }
  return chars.map((c) => ROMA[c] || c).join("-");
}
